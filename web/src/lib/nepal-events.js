/**
 * Get event text for a specific BS date
 * @returns {string|null}
 */
export function getEventForDate(year, month, day) {
  if (year !== 2083) return null;
  const monthEvents = EVENTS_2083[month];
  if (!monthEvents) return null;
  return monthEvents[day] || null;
}

/**
 * Get all events for a BS month
 * @returns {Array<{day: number, event: string}>}
 */
export function getEventsForMonth(year, month) {
  if (year !== 2083) return [];
  const monthEvents = EVENTS_2083[month];
  if (!monthEvents) return [];
  return Object.entries(monthEvents).map(([day, event]) => ({
    day: Number(day),
    event,
  }));
}

